					<h2 class="title">Phosphates...</h2>
                    
					
					<p>Are Phosphates a problem in your swimming pool? <br>
					  Why are phosphates such a problem in swimming pool water?<br>
A: Because when phosphates are present they consume sanitizing chemicals. Phosphate are food for algae and numerous microorganisms. If you have had persistent trouble with low or non-existent chlorine and bromine levels - and this problem seems to keep on continuing, you may have a phosphate problem in your pool When excessive phosphates are present in swimming pool and spa water it creates a unusually high demand on sanitizing chemicals (consumption) and the symptoms often include the following: <br>
					  •	Low or no sanitizer reading<br>
					  •	Cloudy, Green Water<br>
					  •	Slippery and Slimy Surfaces (pink or white mold)<br>
					  •	Mustard and Green Colored Debris (algae and mold)<br>
					  •	Excessive Chemical Consumption<br>
					  •	Poor Water Quality <br>
					  • <br>
					  What are phosphates?<br>
A: Phosphates are pollutants that are environmentally and unintentionally introduced into water....Phosphates are food for algae, molds and other microorganisms. As phosphate levels increase these organisms start to multiply at excessive levels; consuming chlorine and bromine as quickly as it is introduced.</p>
					<p>Where do phosphates come from?<br>
A: They're everywhere…Phosphate is the main ingredient in fertilizers, almost all soaps, detergents, human care products and even pool and spa chemicals used to remove metals and clean filters; in fact many municipal water treatment plants add it to water to protect the plumbing from corrosion. They can quickly accumulate in recreational water and cause numerous problems. In many cases the water used to fill pools and spas may already contain phosphates!</p>
					<p>Why are phosphates even more problematic for chlorine and bromine generators?<br>
A: Filming of the electrode media…Phosphates are known to bind to the cathode of chlorine and bromine generating electrode cells causing a reduction or elimination of bromine and chlorine production, rendering the pool or spa unsanitary.</p>
