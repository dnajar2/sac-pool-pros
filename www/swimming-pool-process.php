		  <div><h3>STEP 1: LAYOUT</h3></div>
		  <div>Before Construction begins, the Layout of your pool or spa must be done. We send out our Layout team to paint and pin the actual shape of your pool or spa onto the ground which will be used as a guide by the excavators. He has all the specifics on pool size, location, and measurements in Cad form which came directly from your pool design contract and drawing. Once the layout is complete, a pre-construction meeting will be scheduled with you and your Sac Pool Construction Superintendent for approval of the pool outline and position. Although changes can be made at this time, we would like you to know beforehand, that this will cause a delay in your construction process and possible additional charges, depending on the changes requested.</div>
		  <div><h3>STEP 2: EXCAVATION</h3></div>
		  <div>Sac Pool Construction &nbsp;has highly experienced excavators to "dig" your swimming pool or spa. The crews are experts in their field, trained for efficiency and safety. Typically, your pool will be excavated in one to two days, depending on the soil conditions they may encounter.&nbsp;</div>
		  <div><h3>STEP 3: PLUMBING &amp; EQUIPMENT SET</h3></div>
		  <div dir="ltr">Sac Pool Construction designs and installs hydraulics that will ensure years of trouble free enjoyment with minimal maintenance to the homeowner. Once the excavation phase has been completed, our team of expert plumbers will be scheduled to install the plumbing lines and pool equipment. Sac Pool Construction has designed your system to be easy to operate, low maintenance and energy efficient, while also providing a pool of sparkling clean water.</div>
		  <div><h3>STEP 4: GAS</h3></div>
		  <div>If your pool or spa has any gas appliances, gas lines will be installed at this time by our highly experienced and certified gas company.</div>
		  <div><h3>STEP 5: STEEL / REBAR</h3></div>
		  <div>Following the plumbing phase will be rebar. A steel frame is installed inside the pool which will reinforce and strengthen the shotcrete shell of your swimming pool. Our rebar company is a leader in their industry, with a team of experienced, hard working, thorough and efficient employees.</div>
		  <div><h3>STEP 6: ELECTRICAL</h3></div>
		  <div>Installation of electric lines follows the steel / rebar phase. The electrical conduit will run from your home breaker box to the pool or spa equipment location and PVC conduit will run from your pool or spa light to your equipment location. Most lines will run underground unless it has been pre-determined as an attic run. In these instances, we usually request the homeowner to be home. The first City Inspection will be called for once the Electric phase has been completed. Our electricians are skilled experts in their field and have an unsurpassed reputation in the Valley.</div>
		  <div><h3>STEP 7: SHOTCRETE</h3></div>
		  <div>Once the electric phase is complete and the city inspection has been done, we will order the shotcrete phase of your pool. Shotcrete is the concrete shell of your pool.</div>
		  <div>&nbsp;</div>
		  <div>Our experienced shotcrete company blends a special mix of shotcrete which is then "shot" into the pool under high pressure through hoses. This phase will require participation of the homeowner for the process of "water curing". In order for your concrete shell to cure properly, it will need to be kept moist during the course of the day. Your Dolphin Superintendent will make sure you understand what needs to be done.</div>
		  <div><h3>STEP 8: MASONRY, ROCK, BOULDER ACCENTS, WATER FEATURES, HARDSCAPES</h3></div>
		  <div>Your pool or spa will need a few days to "water cure" the shotcrete shell, but following the shotcrete comes some of the accent features you may have chosen for your pool or spa.</div>
		  <div>&nbsp;</div>
		  <div>Accent boulders and water features may take a few days depending on size and numbers, but other hardscapes, including fireplaces, BBQ's etc may take longer depending on the complexities involved in their installation. Our hardscape companies do an incredible job, and their work is like artistry in motion.</div>
