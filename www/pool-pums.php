					<h2 class="title">Pool Pumps...</h2>
                    
					<p>Standard pool pumps are equipped with induction motors and offer two speed settings. One of these two speeds must be selected in order to circulate the water in the pool. Though traditional pumps will get the job done, they unfortunately use up a lot of energy. Variable speed pool pumps will also get the job done, but may use as much as 90% less energy than standard varieties.</p>
<p>
<strong>Pentair IntelliFlo® VF Pump<br>
The key features of variable speed pool pumps include:</strong>
<div class="row">
<ul class="list" style=" width:45%;">
<li><i class="fa fa-angle-right"></i>Permanent magnetic motor</li>
<li><i class="fa fa-angle-right"></i>Variable drive technology</li>
<li><i class="fa fa-angle-right"></i>Built in digital intelligence</li>
</ul>
</div>
<p>
These pumps allow for specific programming for specific tasks. Each task, such as running a pool cleaner, will require a specific amount of power. There are thousands of flow rates to select from. By selecting the minimum amount needed to perform each task, energy will be saved. Energy savings of over $1,000 per year are possible once you have switched out your old pump to a new variable speed model. Check out the Pentair website for a calculator showing you just how much you can save by upgrading to one of these variable speed pool pumps.</p>
 
<p>Yes, the pump will run longer when operating at a lower speed. But here is an example a single speed pump runs 8 hours a day in the hot summers of California at 3,450 RPMs so that is equivalent to 3,450 light bulbs on in your house for 8 hours now with a variable speed pump you can run it at lets say 850 RPMS for 16 hours you just turned off over 2,000 light bulbs and saved a ton of money on your energy bill </p>
<p>
While saving energy is certainly advantageous, there are other benefits to consider. Variable speed pumps tend to be much quieter during operation than traditional models. Since the pump will not be forced to operate at higher speeds, it may last much longer than a traditional pump. This lower output may also help other crucial pool parts last longer. A longer lasting pump and pool equipment will certainly work to make a switch a wise investment.
</p>
<figure style="float:left;"><img src="img/eco.gif" width="68" height="85" alt=""/></figure>
<p>
A few more advantages to variable speed pumps are when it comes to running any other features such as solar heating, pool cleaner, spa jets & water features the pump can be set to crank up the RPMS for the desired time to run each feature and then shut it self back down to filtering mode so that its not always running at the high speeds just at your convenience, Built-in diagnostic features allow the unit to in some way protect itself from common problems such as overheating, freezing, or burning up due to power surges.</p>
